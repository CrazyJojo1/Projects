#What are Data frames?
#
#Just like matricies, Data frames are 2-Dimensional objects

#The core difference between the two??
#In Matricies - All the data have to be of the same type. 
#e.g. numeric data only or character data only. You can't have the mix

#Real life is different - you most often found mix

#The mix data can't be imported into a matrix. If you do so, 
#R, by default, will convert those vectors into same type

#This is where you need Data Frames
#Data Frames are objects in R that can result in table. 
#Most importantly, you can have a mix type. 
#A logical type with character type, integer with double type or any mix type of data

#So, Data Frames are most commonly used in analytics to work with any type of data set.




















